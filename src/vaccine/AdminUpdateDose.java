   private void txtICFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtICFocusLost
      if (!txtIC.getText().isBlank()) {
         Vaccine.app = DataIO.checkAppointment(txtIC.getText().trim());
         if (Vaccine.app == null) {
            txtName.setText("");
            JOptionPane.showMessageDialog(txtIC, "This user does not have an appointment");
         } else {
            txtIC.setText(Vaccine.app.getPerson().getIcno());
            txtName.setText(Vaccine.app.getPerson().getName());
            if (Vaccine.app.isDose1()) {
               chbDose1.setSelected(true);
               btnDose1.setEnabled(false);
            }
            if (!Vaccine.app.isDose1() && !Vaccine.app.isDose2()) {
               btnDose2.setEnabled(false);
               chbDose1.setSelected(false);
               chbDose2.setSelected(false);

            }
            if (Vaccine.app.isDose2()) {
               chbDose2.setSelected(true);
               btnDose2.setEnabled(false);

            }
         }
      }
   }//GEN-LAST:event_txtICFocusLost

   private void btnDose1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDose1ActionPerformed
      int confirm = JOptionPane.showConfirmDialog(this, "1st Dose Received?", "Update Dose 1?", JOptionPane.YES_NO_OPTION);
      if (confirm == JOptionPane.YES_OPTION) {
         if (!txtIC.getText().isBlank()) {
            Vaccine.app = DataIO.checkAppointment(txtIC.getText().trim());
            if (Vaccine.app.isDose1()) {
               JOptionPane.showMessageDialog(btnDose1, "User already has completed their 1st dose");
            } else if (Vaccine.app.isDose2()) {
               JOptionPane.showMessageDialog(btnDose1, "User is already fully vaccinated");
            } else {
               chbDose1.setSelected(true);
               btnDose1.setEnabled(false);
               btnDose2.setEnabled(true);
               Vaccine.app.setDose1(true);
               DataIO.write();
            }
         } else {
            JOptionPane.showMessageDialog(btnDose1, "Please fill in the IC field");
         }
      }
   }//GEN-LAST:event_btnDose1ActionPerformed

   private void btnDose2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDose2ActionPerformed
      int confirm = JOptionPane.showConfirmDialog(this, "2nd Dose Received?", "Update Dose 2?", JOptionPane.YES_NO_OPTION);
      if (confirm == JOptionPane.YES_OPTION) {
         if (!txtIC.getText().isBlank()) {
            Vaccine.app = DataIO.checkAppointment(txtIC.getText().trim());
            if (Vaccine.app.isDose1()) {
               chbDose2.setSelected(true);
               btnDose2.setEnabled(false);
               Vaccine.app.setDose2(true);
               DataIO.write();
            } else {
               JOptionPane.showMessageDialog(btnDose2, "Please complete the 1st dose before updating this button");
            }
         } else {
            JOptionPane.showMessageDialog(btnDose2, "Please fill in the IC field");
         }
      }
   }//GEN-LAST:event_btnDose2ActionPerformed

